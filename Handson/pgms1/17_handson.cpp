// C++ program to demonstrate floor function 
#include <iostream> 
#include <cmath> 
using namespace std; 

// Driver function 
int main() 
{ 
	// using floor function which return 
	// floor of input value 
	cout << "Floor is : " << floor(2.3) << endl; 
	cout << "Floor is : " << floor(-2.3) << endl; 

	return 0; 
} 

